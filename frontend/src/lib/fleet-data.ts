function cloneWithId(template: Omit<Vehicle, 'id'>, id: string, overrides?: Partial<Vehicle>): Vehicle {
  return { ...template, id, ...overrides };
}

/** 24 vehicles for catalogue */
export const fleetVehicles: Vehicle[] = [
  ...baseVehicles.map((v, i) => cloneWithId(v, String(i + 1))),
  ...baseVehicles.slice(0, 12).map((v, i) =>
    cloneWithId(v, String(i + 13), {
      name: `${v.name} II`,
      pricePerDay: v.pricePerDay + (i % 3) * 5,
      year: v.year - (i % 2),
      popularity: v.popularity - (i % 5),
      mileage: v.mileage + 5000,
    }),
  ),
];

export function getVehicleById(id: string): Vehicle | undefined {
  return fleetVehicles.find((v) => v.id === id);
}

